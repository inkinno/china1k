# modeling.txt를 읽고, 파싱하여 1000자 전체 한자 리스트를 복원한 뒤, 단어 모델링을 조립합니다.
def generate_word_data():
    modeling_path = r'c:\ai\apps\0_inkinno\01_sitesnew\china1k\plan\CN1K\modeling.txt'
    output_path = r'c:\ai\apps\0_inkinno\01_sitesnew\china1k\plan\CN1K\word_modeling.txt'
    
    if not os.path.exists(modeling_path):
        print(f"Error: {modeling_path} not found.")
        return
        
    with open(modeling_path, 'r', encoding='utf-8') as f:
        content = f.read()
        
    # 한 줄씩 정밀하게 분석하여 객체를 파싱 (순서 무관 매칭)
    lines = content.split('\n')
    word_modeling_list = []
    
    for idx, line in enumerate(lines):
        line = line.strip()
        if not line or 'id:' not in line or 'hanja:' not in line:
            continue
            
        # 개별 필드 정규표현식 매칭
        id_match = re.search(r'id:\s*(\d+)', line)
        hanja_match = re.search(r"hanja:\s*'([^']+)'", line)
        sound_match = re.search(r"sound:\s*'([^']+)'", line)
        meaning_match = re.search(r"meaning:\s*'([^']+)'", line)
        stroke_match = re.search(r'stroke:\s*(\d+)', line)
        tag_match = re.search(r'tag:\s*(\d+)', line)
        
        if not (id_match and hanja_match and sound_match and meaning_match and stroke_match and tag_match):
            print(f"Skipped malformed line {idx+1}: {line}")
            continue
            
        item_id = int(id_match.group(1))
        hanja = hanja_match.group(1)
        sound = sound_match.group(1)
        meaning = meaning_match.group(1)
        stroke = int(stroke_match.group(1))
        tag = int(tag_match.group(1))
        
        # WORD_DB에 있는지 조회, 없으면 지능적 기본값 조합으로 안전 구축(Fallback)
        if hanja in WORD_DB:
            word_hanja, word_hangul, word_meaning = WORD_DB[hanja]
        else:
            # Fallback 로직: 한자 + '人'(인) 또는 '物'(물) 또는 '的'(적) 등 매칭
            if tag <= 2:
                word_hanja = hanja + '人'
                word_hangul = sound + '인'
                word_meaning = f"실생활에 쓰이는 '{sound}' 관련 인물 또는 대상"
            else:
                word_hanja = hanja + '物'
                word_hangul = sound + '물'
                word_meaning = f"실생활에 쓰이는 '{sound}' 관련 물건이나 상태"
                
        # 단어의 총 획수 계산 (임시적으로 한자 획수에 6획을 더하여 난이도를 맞춤)
        word_stroke = stroke + 6
        
        word_item = {
            "id": item_id,
            "hanja": hanja,
            "wordHanja": word_hanja,
            "wordHangul": word_hangul,
            "meaning": word_meaning,
            "stroke": word_stroke,
            "tag": tag
        }
        word_modeling_list.append(word_item)
        
    print(f"Parsed {len(word_modeling_list)} items out of lines.")
    
    # ID 중복 및 누락 점검
    existing_ids = set(x['id'] for x in word_modeling_list)
    missing_ids = sorted(list(set(range(1, 1001)) - existing_ids))
    if missing_ids:
        print(f"Warning: Missing IDs: {missing_ids}")
    else:
        print("Success: All 1000 IDs are present without any gaps!")
        
    # JSON 형식으로 예쁘게 포맷팅하여 word_modeling.txt로 출력 및 백업
    with open(output_path, 'w', encoding='utf-8') as f:
        # JS ES모듈 형태로 쉽게 로딩 가능하도록 export const CHUNJA_WORD_DATA = ... 형식으로 저장
        f.write("// 실생활 한자 단어 모델링 데이터 (천자문 ID 1~1000 연동)\n")
        f.write("export const CHUNJA_WORD_DATA = ")
        json.dump(word_modeling_list, f, ensure_ascii=False, indent=2)
        f.write(";\n")
        
    print(f"Successfully generated word modeling database to: {output_path}")
    print(f"Total entries: {len(word_modeling_list)}")

if __name__ == "__main__":
    generate_word_data()
